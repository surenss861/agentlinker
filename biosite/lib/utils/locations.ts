// Countries
export const COUNTRIES = [
  { code: 'US', name: 'United States', currency: 'USD', currencySymbol: '$' },
  { code: 'CA', name: 'Canada', currency: 'CAD', currencySymbol: 'C$' },
]

// Get regions based on country
export function getRegions(country: string) {
  if (country === 'US') return US_STATES
  if (country === 'CA') return CANADIAN_PROVINCES
  return []
}

// Get region label based on country
export function getRegionLabel(country: string) {
  if (country === 'US') return 'State'
  if (country === 'CA') return 'Province'
  return 'State/Province'
}

// Get postal code label based on country
export function getPostalCodeLabel(country: string) {
  if (country === 'US') return 'ZIP Code'
  if (country === 'CA') return 'Postal Code'
  return 'ZIP/Postal Code'
}

// Format phone number based on country
export function formatPhoneNumber(phone: string, country: string) {
  const cleaned = phone.replace(/\D/g, '')
  
  // US/Canada format: (123) 456-7890
  if ((country === 'US' || country === 'CA') && cleaned.length === 10) {
    return `(${cleaned.slice(0, 3)}) ${cleaned.slice(3, 6)}-${cleaned.slice(6)}`
  }
  
  // US/Canada format with country code: +1 (123) 456-7890
  if ((country === 'US' || country === 'CA') && cleaned.length === 11 && cleaned.startsWith('1')) {
    return `+1 (${cleaned.slice(1, 4)}) ${cleaned.slice(4, 7)}-${cleaned.slice(7)}`
  }
  
  return phone
}

// Get currency info based on country
export function getCurrencyInfo(country: string) {
  const countryInfo = COUNTRIES.find(c => c.code === country)
  return countryInfo || COUNTRIES[0]
}

